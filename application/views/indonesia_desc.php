						<h2><span class="glyphicon glyphicon-align-justify" aria-hidden="true"></span> Description</h2>

						<p>Yogyakarta (or Jogjakarta) known as The Neverending Asia. Things you can experience in Jogja may seem overwhelming, ranging from natural splendors, art and tradition and heritages to culinary adventure. This is why Jogja is the second most visited destination in Indonesia, next to Bali.</p>

						<p>In addition, there are about 70,000 handicraft industries and other facilities like various accommodations and transportations, numerous food services, travel agents, and proper tourism support, and also tour security team support called as Policemen of Tour, locally known as  BhayangkaraWisata.</p>

						<p>Jogjakarta's geographical condition also supports the variety of existing tourism objects. Friendly climate ensures that you can plan your trips more intensely. The beautiful landscape along the way makes your travel to each destination worthwhile.</p>

						<p>You can also try Selarong Cave, Pandansimo beach, Gajah Mountain, or Vredeburg Fort. To understand the history of the sultanate, try visiting the Kraton of Yogyakarta and Tamansari. A visit to Jogja is never complete without experiencing Malioborostreet. Rows of shops and outlets sell many kinds of souvenirs you can bring back home. If you want to test your negotiation skills you can try The Haggling Game with the street vendors. All's fair in love and shopping.</p>
